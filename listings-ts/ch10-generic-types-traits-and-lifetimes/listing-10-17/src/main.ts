// 라이프타임 주석 예시
// Rust에서 'a, 'b로 표현하는 라이프타임은 변수의 유효 범위를 나타냄
function main() {
    let r: number;           // ---------+-- 'a (r의 라이프타임)
                              //          |
    {                         //          |
        const x = 5;          // -+-- 'b  | (x의 라이프타임)
        r = x;                //  |       | // Rust: r = &x (참조)
    }                         // -+       | // x 스코프 종료 ('b 끝)
                              //          |
    console.log(`r: ${r}`);   //          |
}                             // ---------+ // 'a 끝
// Rust: 'b < 'a 이므로, r이 x의 참조를 가지면 댕글링 참조!
