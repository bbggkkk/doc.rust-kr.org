// JS에서는 이동(move)이 없습니다 — GC가 메모리를 관리합니다
let s1 = "hello";
let s2 = s1;       // JS: s1과 s2 모두 같은 값을 참조 ✅
console.log(s1);   // JS: 정상 동작 ✅
// Rust: s1이 s2로 "이동"되어 s1 사용 불가 ❌ (컴파일 에러)

// 왜 Rust는 이렇게 하나?
// → GC 없이도 메모리를 안전하게 관리하기 위해
// → 두 변수가 같은 힙 데이터를 가리키면 중복 해제(double free) 위험
// → JS는 GC가 참조 카운트를 추적하여 이 문제를 해결
