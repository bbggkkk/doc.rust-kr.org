// 순환 참조와 메모리 누수
// Rust: Rc<T>로 순환 참조를 만들면 참조 카운트가 0이 되지 않아 메모리 누수 발생
// JS: GC의 mark-and-sweep 알고리즘이 순환 참조를 자동으로 처리합니다

// JS에서 순환 참조 만들기 — GC가 자동 처리
const a = { value: "a" };
const b = { value: "b" };
a.next = b;
b.next = a; // 순환 참조!

// JS의 현대 GC(mark-and-sweep)는 이를 감지하고 해제합니다.
// 루트에서 도달할 수 없으면 수집 대상이 됩니다.

// 단, 과거 IE의 참조 카운팅 GC에서는 순환 참조가 메모리 누수를 발생시켰습니다.
// Rust의 Rc<T>도 참조 카운팅 방식이므로 같은 문제가 있습니다.
