// Box<T>: 힙에 데이터 저장하기
// JS에서는 원시 타입(number, string 등)을 제외한 모든 객체가
// 자동으로 힙에 할당됩니다. Box<T>에 해당하는 개념이 필요 없습니다.

// Rust: let b = Box::new(5);
// JS에서는 그냥 변수에 할당하면 됩니다.
const b = 5; // 원시 타입은 스택
const obj = { value: 5 }; // 객체는 자동으로 힙에 할당

console.log(b); // 5
console.log(obj.value); // 5

// Rust에서 Box가 필요한 이유:
// 1. 컴파일 타임에 크기를 알 수 없는 타입 (재귀적 타입)
// 2. 큰 데이터의 소유권 이전 시 복사 방지
// 3. 트레이트 객체
//
// JS에서는 GC가 메모리를 관리하므로 이런 고려가 필요 없습니다.
