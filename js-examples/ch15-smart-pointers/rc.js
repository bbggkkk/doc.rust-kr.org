// Rc<T>: 참조 카운팅 스마트 포인터
// JS에서는 GC가 참조 카운팅(또는 mark-and-sweep)을 자동으로 처리합니다.
// 명시적인 Rc<T>가 필요 없습니다.

// Rust에서는 여러 소유자가 데이터를 공유하려면 Rc<T>가 필요합니다.
// JS에서는 여러 변수가 같은 객체를 자연스럽게 참조할 수 있습니다.

// 콘스 리스트를 공유하는 예시
const a = [5, 10]; // 공유될 리스트
const b = [3, ...a]; // b가 a의 데이터를 참조 (여기서는 복사)
const c = [4, ...a]; // c도 a의 데이터를 참조 (여기서는 복사)

// 실제 참조 공유 (복사 없이)
const shared = { value: [5, 10] };
const ref1 = shared; // 같은 객체를 참조
const ref2 = shared; // 같은 객체를 참조

console.log(ref1.value); // [5, 10]
console.log(ref2.value); // [5, 10]

// Rust의 Rc::strong_count()에 해당하는 것은 JS에 없습니다.
// GC가 내부적으로 참조를 추적하지만 개발자에게 노출하지 않습니다.

// 차이점:
// - Rust Rc<T>: 참조 카운트가 0이 되면 즉시 해제 (결정적)
// - JS GC: 참조가 없어지면 언젠가 해제 (비결정적)
// - Rust Rc<T>: 읽기 전용 공유만 허용
// - JS: 공유된 객체를 자유롭게 수정 가능 (위험할 수 있음)
